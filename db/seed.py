async def seed_defaults() -> None:
    async with Session() as s:
        existing_keys = {k for (k,) in (await s.execute(select(BotText.key))).all()}
        for t in TEXTS:
            if t["key"] not in existing_keys:
                s.add(BotText(key=t["key"], title=t["title"], text=t["text"],
                              buttons=t.get("buttons", [])))

        rules_count = (await s.execute(select(func.count(FollowUpRule.id)))).scalar()
        if not rules_count:
            for trigger, step, delay, key, stops in FOLLOWUP_RULES:
                s.add(FollowUpRule(trigger_state=trigger, step_order=step, delay_hours=delay,
                                   text_key=key, stop_states=stops))

        def _mk(q: dict, order: int) -> SurveyQuestion:
            return SurveyQuestion(
                order=order, text=q["text"], field_type=q.get("type", "text"),
                options=q.get("options", []), required=q.get("required", True),
                stage=q.get("stage", "main"), disqualify_if=q.get("disqualify_if", []),
                intro_key=q.get("intro_key", ""))

        q_count = (await s.execute(select(func.count(SurveyQuestion.id))
                                   .where(SurveyQuestion.stage != "manager"))).scalar()
        if not q_count:
            # Чистая БД — сидируем всю staged-анкету целиком
            for i, q in enumerate(SURVEY_QUESTIONS, start=1):
                s.add(_mk(q, i))
            await s.flush()  # менеджерский блок должен получить order ПОСЛЕ основного
        else:
            # Существующая БД. Если это старая БД до staged-анкеты (нет блоков фильтра/
            # паспорта/выбора аудита) — досидируем их ПЕРЕД основными вопросами, иначе
            # фильтр масштаба и углублённый аудит там недостижимы.
            has_qualify = (await s.execute(select(func.count(SurveyQuestion.id))
                                           .where(SurveyQuestion.stage == "qualify"))).scalar()
            if not has_qualify:
                prefix = [q for q in SURVEY_QUESTIONS
                          if q.get("stage") in ("qualify", "passport", "audit_choice")]
                k = len(prefix)
                # сдвигаем существующие вопросы, освобождая orders 1..k под префикс
                await s.execute(update(SurveyQuestion)
                                .values(order=SurveyQuestion.order + k))
                for i, q in enumerate(prefix, start=1):
                    s.add(_mk(q, i))
                await s.flush()

        # Менеджерский блок сидируется отдельно — добавляется и в уже работающие БД
        mgr_count = (await s.execute(select(func.count(SurveyQuestion.id))
                                     .where(SurveyQuestion.stage == "manager"))).scalar()
        if not mgr_count:
            max_order = (await s.execute(
                select(func.coalesce(func.max(SurveyQuestion.order), 0)))).scalar()
            for i, text in enumerate(MANAGER_QUESTIONS, start=1):
                s.add(SurveyQuestion(
                    order=max_order + i,
                    text=text,
                    field_type="text",
                    required=(i != 2),  # ссылки на профиль — необязательные
                    stage="manager",
                ))

        # --- Миграции существующих БД (идемпотентно; правки админа не затираем) ---
        # 1) Полосы оборота: старый фильтр отсекал всё «До $500 тыс.» → непроходная только
        #    «До $100 тыс.». Трогаем вопрос лишь со СТАРЫМ набором вариантов.
        turnover_q = (await s.execute(
            select(SurveyQuestion)
            .where(SurveyQuestion.stage == "qualify")
            .where(SurveyQuestion.text.like("Объём ежемесячного оборота%")))).scalars().first()
        if turnover_q is not None and list(turnover_q.options or []) == TURNOVER_OPTIONS_OLD:
            turnover_q.options = TURNOVER_OPTIONS
            turnover_q.disqualify_if = TURNOVER_DISQUALIFY

        # 2) Кнопки up-front слотов: убрать «в группу» — группа только после фильтра.
        #    (Админ-редактор кнопки не меняет, поэтому правим здесь; только если набор старый.)
        for key, old_btns, new_btns in (
            ("welcome", WELCOME_BTNS_OLD, WELCOME_BTNS),
            ("philosophy_3", PHIL3_BTNS_OLD, PHIL3_BTNS),
            ("survey_reject", REJECT_BTNS_OLD, REJECT_BTNS),
            # Добавить кнопку продажника «Обсудить разбор» (go:closer) в горячие слоты.
            ("qualified_hub", QUALIFIED_HUB_BTNS_OLD, QUALIFIED_HUB_BTNS),
            ("survey_thanks", SURVEY_THANKS_BTNS_OLD, SURVEY_THANKS_BTNS),
        ):
            bt = (await s.execute(select(BotText).where(BotText.key == key))).scalars().first()
            if bt is not None and bt.buttons == old_btns:
                bt.buttons = new_btns

        # 3) Текст слотов, куда добавили кнопку продажника: подтягиваем к новому
        #    дефолту (упоминает консультанта), но ТОЛЬКО если текст ещё старый —
        #    правки владельца через админку не затираем.
        _new_texts = {t["key"]: t["text"] for t in TEXTS}
        for key, old_text in (("qualified_hub", QUALIFIED_HUB_TEXT_OLD),
                              ("survey_thanks", SURVEY_THANKS_TEXT_OLD)):
            bt = (await s.execute(select(BotText).where(BotText.key == key))).scalars().first()
            if bt is not None and bt.text == old_text and key in _new_texts:
                bt.text = _new_texts[key]

        existing_settings = {k for (k,) in (await s.execute(select(Setting.key))).all()}
        for k, v in SETTINGS_DEFAULTS.items():
            if k not in existing_settings:
                s.add(Setting(key=k, value=v))

        await s.commit()
